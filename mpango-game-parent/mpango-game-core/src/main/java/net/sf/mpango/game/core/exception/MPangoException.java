package net.sf.mpango.game.core.exception;

public class MPangoException extends Exception {

	private static final long serialVersionUID = 6703199019259794290L;

	public MPangoException() {
		super();
	}

	public MPangoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MPangoException(String arg0) {
		super(arg0);
	}

	public MPangoException(Throwable arg0) {
		super(arg0);
	}

}
